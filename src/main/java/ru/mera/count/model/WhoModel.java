package ru.mera.count.model;

public class WhoModel {
	private String who;
	private int count;

	public String getWho() {
		return who;
	}

	public void setWho(String who) {
		this.who = who;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
}
